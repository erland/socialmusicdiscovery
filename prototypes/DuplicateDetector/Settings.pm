package Plugins::DuplicateDetector::Settings;

use strict;
use base qw(Plugins::DuplicateDetector::BaseSettings);

use File::Basename;
use File::Next;

use Slim::Utils::Log;
use Slim::Utils::Prefs;
use Slim::Utils::Misc;
use Slim::Utils::Strings;

my $prefs = preferences('plugin.duplicatedetector');
my $log   = logger('plugin.duplicatedetector');

my $plugin; # reference to main plugin

sub new {
	my $class = shift;
	$plugin   = shift;

	$class->SUPER::new($plugin,1);
}

sub name {
	return 'PLUGIN_DUPLICATEDETECTOR';
}

sub page {
	return 'plugins/DuplicateDetector/settings/basic.html';
}

sub currentPage {
	return Slim::Utils::Strings::string('PLUGIN_DUPLICATEDETECTOR_SETTINGS');
}

sub pages {
	my %page = (
		'name' => name(),
		'page' => page(),
	);
	my @pages = (\%page);
	return \@pages;
}

sub prefs {
        return ($prefs, qw(noofbytes));
}

sub handler {
	my ($class, $client, $paramRef) = @_;

	return $class->SUPER::handler($client, $paramRef);
}

		
1;
