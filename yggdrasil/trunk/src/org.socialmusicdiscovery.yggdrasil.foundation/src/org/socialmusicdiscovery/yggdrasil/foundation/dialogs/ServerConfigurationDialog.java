package org.socialmusicdiscovery.yggdrasil.foundation.dialogs;

import org.eclipse.jface.dialogs.Dialog;
import org.eclipse.jface.dialogs.IDialogConstants;
import org.eclipse.swt.SWT;
import org.eclipse.swt.graphics.Point;
import org.eclipse.swt.layout.GridData;
import org.eclipse.swt.widgets.Composite;
import org.eclipse.swt.widgets.Control;
import org.eclipse.swt.widgets.Label;
import org.eclipse.swt.widgets.Shell;
import org.eclipse.swt.widgets.Text;

/**
 * @author Peer TÃ¶rngren
 *
 */
public class ServerConfigurationDialog extends Dialog {
	private Text textRoots;
	private String title;

	/**
	 * Create the dialog.
	 * @param parentShell
	 */
	public ServerConfigurationDialog(Shell parentShell, String dialogTitle) {
		super(parentShell);
		this.title = dialogTitle;
	}

	/**
	 * Create contents of the dialog.
	 * @param parent
	 */
	@Override
	protected Control createDialogArea(Composite parent) {
		getShell().setText(title);
		Composite container = (Composite) super.createDialogArea(parent);
		
		Label lblWorkInProgress = new Label(container, SWT.NONE);
		lblWorkInProgress.setLayoutData(new GridData(SWT.CENTER, SWT.CENTER, false, false, 1, 1));
		lblWorkInProgress.setText("WORK IN PROGRESS - NOT YET IMPLEMENTED!!!");
		
		Label labelRoots = new Label(container, SWT.NONE);
		labelRoots.setLayoutData(new GridData(SWT.FILL, SWT.CENTER, true, false, 1, 1));
		labelRoots.setText("Root Folders:");
		
		textRoots = new Text(container, SWT.BORDER);
		textRoots.setLayoutData(new GridData(SWT.FILL, SWT.FILL, true, false, 1, 1));

		return container;
	}

	/**
	 * Create contents of the button bar.
	 * @param parent
	 */
	@Override
	protected void createButtonsForButtonBar(Composite parent) {
		createButton(parent, IDialogConstants.OK_ID, IDialogConstants.OK_LABEL, true);
		createButton(parent, IDialogConstants.CANCEL_ID, IDialogConstants.CANCEL_LABEL, false);
	}

	/**
	 * Return the initial size of the dialog.
	 */
	@Override
	protected Point getInitialSize() {
		return new Point(450, 300);
	}

}
